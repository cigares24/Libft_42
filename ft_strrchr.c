#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int		n;

	n = 0;
	while (s[n])
		n++;
	while (n >= 0)
	{
		if (s[n] == c)
			return (s + n);
		n--;
	}
	return (NULL);
}
